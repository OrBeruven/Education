int Bursa(int arr[], int size, int indexex[2]) 
{
    int curr_max = 0;
    int best_max = 0;

    int sell = 0;
    int buy = 0;

    for (sell = 1; sell < size; ++sell){
        curr_max += arr[sell] - arr[sell - 1];

        if (curr_max < 0) {
            curr_max = 0;
            buy = sell;
        } 
        else if (curr_max > best_max) {
            best_max = curr_max;
            indexex[0] = buy;
            indexex[1] = sell;
        }
    }

    return best_max;
}

